

public class User {
String uname;
String upass;
String email;
long num;
public String getUname() {
	return uname;
}
public void setUname(String uname) {
	this.uname = uname;
}
public String getUpass() {
	return upass;
}
public void setUpass(String upass) {
	this.upass = upass;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public long getNum() {
	return num;
}
public void setNum(long num) {
	this.num = num;
}

}
